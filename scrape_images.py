async def download_images(
    image_urls: set[str],
    output_dir: Path,
    concurrency: int = 8,
) -> tuple[list[Path], list[tuple[str, str]]]:
    """Download all collected images concurrently."""
    output_dir.mkdir(parents=True, exist_ok=True)
    sem = asyncio.Semaphore(concurrency)
    downloaded: list[Path] = []
    failed: list[tuple[str, str]] = []

    async def fetch(client: httpx.AsyncClient, url: str, progress, task) -> None:
        async with sem:
            try:
                r = await client.get(url, follow_redirects=True, timeout=20)
                r.raise_for_status()
                ct = r.headers.get("content-type", "")
                ext = guess_extension(ct, url)
                filename = slugify_url(url)
                # ensure correct extension
                if not filename.lower().endswith(ext.lower()):
                    filename = Path(filename).stem + ext
                dest = output_dir / filename
                # avoid overwriting identical files
                counter = 1
                while dest.exists():
                    dest = output_dir / f"{Path(filename).stem}_{counter}{ext}"
                    counter += 1
                dest.write_bytes(r.content)
                downloaded.append(dest)
            except Exception as exc:
                failed.append((url, str(exc)))
            finally:
                progress.advance(task)

    with Progress(
        SpinnerColumn(),
        TextColumn("[progress.description]{task.description}"),
        BarColumn(),
        TextColumn("{task.completed}/{task.total}"),
        TimeElapsedColumn(),
        console=console,
    ) as progress:
        task = progress.add_task("[green]Downloading images…", total=len(image_urls))
        async with httpx.AsyncClient(
            headers={"User-Agent": "Mozilla/5.0 image-scraper/1.0"},
            follow_redirects=True,
        ) as client:
            await asyncio.gather(*[fetch(client, u, progress, task) for u in image_urls])

    return downloaded, failed


