def print_report(downloaded: list[Path], failed: list[tuple[str, str]], output_dir: Path) -> None:
    console.rule("[bold green]Scraping Complete")
    table = Table(show_header=True, header_style="bold magenta")
    table.add_column("Metric", style="cyan", no_wrap=True)
    table.add_column("Value", style="white")
    table.add_row("✅ Downloaded", str(len(downloaded)))
    table.add_row("❌ Failed", str(len(failed)))
    table.add_row("📁 Output folder", str(output_dir.resolve()))
    total_bytes = sum(p.stat().st_size for p in downloaded if p.exists())
    table.add_row("💾 Total size", f"{total_bytes / 1024:.1f} KB" if total_bytes < 1_048_576 else f"{total_bytes / 1_048_576:.2f} MB")
    console.print(table)

    if failed:
        console.rule("[bold red]Failed URLs")
        for url, reason in failed[:20]:
            rprint(f"  [red]✗[/red] {url[:80]}…  →  [dim]{reason}[/dim]")
        if len(failed) > 20:
            console.print(f"  … and {len(failed) - 20} more.")


