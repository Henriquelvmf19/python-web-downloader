async def main() -> None:
    import argparse

    parser = argparse.ArgumentParser(
        description="🖼️  Download all images from a JavaScript-rendered website (React, Vite, Next.js, etc.)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python image_scraper.py https://example.com
  python image_scraper.py https://example.com -o ./downloads --no-scroll
  python image_scraper.py https://example.com -c 16 --timeout 30
        """,
    )
    parser.add_argument("url", help="Target URL to scrape")
    parser.add_argument("-o", "--output", default="./images", help="Output directory (default: ./images)")
    parser.add_argument("-c", "--concurrency", type=int, default=8, help="Parallel download workers (default: 8)")
    parser.add_argument("--no-scroll", action="store_true", help="Disable auto-scroll (faster, may miss lazy images)")
    parser.add_argument("--timeout", type=int, default=15, help="Page load timeout in seconds (default: 15)")
    parser.add_argument("--min-size", type=int, default=0, help="Skip images smaller than N bytes after download (default: 0)")
    args = parser.parse_args()

    output_dir = Path(args.output)
    console.rule(f"[bold blue]Image Scraper[/bold blue]")
    console.print(f"[bold]Target:[/bold] {args.url}")
    console.print(f"[bold]Output:[/bold] {output_dir.resolve()}\n")

    # Step 1 — collect
    image_urls = await collect_image_urls(
        args.url,
        scroll=not args.no_scroll,
        timeout=args.timeout * 1000,
    )
    console.print(f"\n[bold green]{len(image_urls)} image URL(s) found.[/bold green]\n")

    if not image_urls:
        console.print("[yellow]No images found. Try increasing --timeout or check if the site requires authentication.[/yellow]")
        sys.exit(0)

    # Step 2 — download
    downloaded, failed = await download_images(image_urls, output_dir, concurrency=args.concurrency)

    # Step 3 — optional size filter
    if args.min_size > 0:
        removed = [p for p in downloaded if p.exists() and p.stat().st_size < args.min_size]
        for p in removed:
            p.unlink()
        downloaded = [p for p in downloaded if p not in removed]
        if removed:
            console.print(f"[yellow]Removed {len(removed)} image(s) smaller than {args.min_size} bytes.[/yellow]")

    print_report(downloaded, failed, output_dir)


if __name__ == "__main__":
    asyncio.run(main())