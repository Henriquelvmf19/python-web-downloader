def slugify_url(url: str) -> str:
    """Turn a URL into a safe filename, preserving the original extension."""
    parsed = urlparse(url)
    name = Path(parsed.path).name or "image"
    # strip query strings from extension detection
    name_clean = name.split("?")[0]
    ext = Path(name_clean).suffix or ".jpg"
    digest = hashlib.md5(url.encode()).hexdigest()[:8]
    stem = re.sub(r"[^\w\-]", "_", Path(name_clean).stem)[:40]
    return f"{stem}_{digest}{ext}"


def guess_extension(content_type: str, url: str) -> str:
    """Return a file extension based on Content-Type or URL."""
    ct = content_type.split(";")[0].strip().lower()
    ext = mimetypes.guess_extension(ct)
    # mimetypes sometimes returns .jpe instead of .jpg
    mapping = {".jpe": ".jpg", ".jpeg": ".jpg", None: Path(urlparse(url).path).suffix or ".jpg"}
    return mapping.get(ext, ext)


def is_image_url(url: str) -> bool:
    """Heuristic: does this URL look like an image?"""
    IMAGE_EXTS = {".png", ".jpg", ".jpeg", ".gif", ".webp", ".svg", ".avif", ".ico", ".bmp", ".tiff"}
    IMAGE_PATTERNS = ["image/", "/assets/", "/images/", "/img/", "/media/", "/static/"]
    path = urlparse(url).path.lower()
    if any(path.endswith(ext) for ext in IMAGE_EXTS):
        return True
    if any(pat in url.lower() for pat in IMAGE_PATTERNS):
        return True
    return False


def resolve_url(base: str, src: str) -> str | None:
    """Resolve relative or protocol-relative URLs."""
    if not src or src.startswith("data:"):
        return None
    if src.startswith("//"):
        src = "https:" + src
    return urljoin(base, src)


